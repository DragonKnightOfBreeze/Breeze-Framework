package com.windea.breezeframework.core.consts

/**常用字符串的常量集。*/
object Strings {
	const val empty: String = ""
	const val space: String = " "
	const val space2: String = "  "
	const val space4: String = "    "

	const val singleQuote: String = "'"
	const val doubleQuote: String = "\""
	const val backQuote: String = "`"

	const val lSep: String = "\\"
	const val rSep: String = "/"

	const val tab: String = "\t"
	const val cr: String = "\r"
	const val lf: String = "\n"
	const val crlf: String = "\r\n"
}
